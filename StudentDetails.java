class StudentDetails
{
public static void main(String args[])
{
int mark=90;
if(mark>=90)
{
System.out.println("Student grade is A+");
}
else if(mark>=50)
{
System.out.println("Student grade is D");
}
else if(mark>=40)
{
System.out.println("Student grade is C");
}
else
{

System.out.println("Student failed");
}
}
}

